package src;

public class Lab005_Loop {
    public static void main(String[] args) {
        //loop : For Loop / While loop /Do while loop

        //for loop perform  a task multiple time // Base on condition  -> task multiple times


        for (int i = 20; i >0 ; i--) {
            if(i%2==1) {
                System.out.println("Print odd" +i);

            }
        }
        System.out.println("End of program!!");
    }

}
